#include "libft.h"

void	ft_print_bits(size_t octet)
{
	int	i;

	i = 7;
	while (i >= 0)
	{
		if (octet & (1 << i))
			ft_putchar('1');
		else
			ft_putchar('0');
		i--;
	}
}

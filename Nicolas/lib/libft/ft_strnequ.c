#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t size)
{
	size_t	i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[i] == s2[i] && i < size && s1[i] && s2[i])
		i++;
	if ((s1[i] == '\0' && s2[i] == '\0') || i == size)
		return (1);
	return (0);
}

#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*c1;
	char	*c2;

	c1 = s1;
	c2 = (char *)s2;
	while (*c1 != '\0')
		c1++;
	while (*c2 != '\0')
	{
		*c1 = *c2;
		c1++;
		c2++;
	}
	*c1 = '\0';
	return (s1);
}

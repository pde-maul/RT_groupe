#include "libft.h"

int		ft_atoi(char *str)
{
	int i;
	int nbr;
	int neg;

	i = 0;
	nbr = 0;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' ||
			str[i] == ' ' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] && (str[i] == '+' || str[i] == '-'))
	{
		neg = str[i] == '-' ? 1 : 0;
		i++;
	}
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		nbr *= 10;
		nbr += str[i] - '0';
		i++;
	}
	return (neg == 1 ? -nbr : nbr);
}
